// 计算神兽提供的总加成（在战斗属性计算中调用）
function calculateEquippedBeastBonus() {
    let totalBonus = {
        health: 0, // 生命加成（改为加法累加）
        attack: 0, // 攻击加成（改为加法累加）
        critDamage: 0 // 爆伤加成（改为加法累加）
    };
    
    // 只计算已装备的神兽
    player.beasts.equipped.forEach(beastId => {
        const beast = player.beasts.inventory.find(b => b.id === beastId);
        if (beast && Array.isArray(beast.affixes) && playerCanEquipBeast(beast)) {
            beast.affixes.forEach(affix => {
                if (!affix || affix.type == null) return;
                const bonusType = getBonusType(affix.type);
                if (totalBonus[bonusType] !== undefined) {
                    totalBonus[bonusType] += affix.value;
                }
            });
        }
    });
    
    // 按等级实时计算共享加成，避免旧存档倍率字段未更新导致显示/实际不一致
    const shareLevel = Number(player.beasts?.shareLevel?.level || 0);
    const shareBonus = 1 + (shareLevel * 1);
    if (player.beasts && player.beasts.shareLevel) {
        player.beasts.shareLevel.bonusMultiplier = shareBonus;
    }
    totalBonus.health *= shareBonus;
    totalBonus.attack *= shareBonus;
    totalBonus.critDamage *= shareBonus;
    
    return totalBonus;
}

/** 联网至尊神器：加成与轮回神兽同类词条（生命/攻击/爆伤），无穿戴限制 */
function calculateEquippedSupremeArtifactBonus() {
    var totalBonus = { health: 0, attack: 0, critDamage: 0 };
    if (typeof getGoldGameAuthToken !== 'function' || !getGoldGameAuthToken()) return totalBonus;
    var eq = window._supremeArtifactsCache && window._supremeArtifactsCache.equipped;
    if (!eq || typeof eq !== 'object') return totalBonus;
    Object.keys(eq).forEach(function(slotId) {
        var art = eq[slotId];
        if (!art || !Array.isArray(art.affixes)) return;
        art.affixes.forEach(function(affix) {
            if (!affix || affix.type == null) return;
            var bonusType = getBonusType(affix.type);
            if (totalBonus[bonusType] !== undefined) totalBonus[bonusType] += Number(affix.value) || 0;
        });
    });
    return totalBonus;
}

// 辅助函数：词条类型映射
function getBonusType(chineseType) {
    const typeMap = {
        '生命加成': 'health',
        '攻击加成': 'attack', 
        '爆伤加成': 'critDamage'
    };
    return typeMap[chineseType] || 'health';
}
function getEggsRequiredForNextLevel(currentLevel) {
    // 每级基础消耗20个，每10级额外增加30个
    const baseCost = 20;
    const extraCost = Math.floor(currentLevel / 10) * 30;
    return baseCost + extraCost;
}

// 计算升级到指定等级所需的总神兽蛋
function getEggsRequiredForLevels(fromLevel, toLevel) {
    let totalEggs = 0;
    for (let level = fromLevel; level < toLevel; level++) {
        totalEggs += getEggsRequiredForNextLevel(level);
    }
    return totalEggs;
}

